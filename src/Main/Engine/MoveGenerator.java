	@Override
	public short[] collectMoves(boolean toMove, short[] allMoves) {
		allMoves[0] = 0;
		collectCaptures(toMove, allMoves);

		if (toMove) {
			for (int square = 0; square < 164; square++) {
				if (board.getSquare(square) == 1) {
					dwarfMoves(square, allMoves);
				}
			}
		} else {
			for (int square = 0; square < 164; square++) {
				if (board.getSquare(square) == -1) {
					trollMoves(square, allMoves);
				}
			}
		}
		return allMoves;
	}

	private void dwarfMoves(int square, short[] allMoves) {
		createSlidingMoves(diagonalsForDwarfs, square, allMoves);
		createSlidingMoves(rowsForDwarfs, square, allMoves);
	}

	private void createSlidingMoves(byte[][] array, int square, short[] allMoves) {
		for (int direction = 0; direction < 4; direction++) {
			byte[] sliding = array[square * 4 + direction];
			for (byte aSliding : sliding) {
				if (board.getSquare(Byte.toUnsignedInt(aSliding)) == 0) {
					allMoves[++allMoves[0]] = (short) ((square << 8) + (aSliding & 0xFF));
				} else {
					break;
				}
			}
		}
	}

	private void trollMoves(int square, short[] allMoves) {
		byte[] neighbors = neighborSquares[square];
		for (byte neighbor : neighbors) {
			if (board.getSquare(Byte.toUnsignedInt(neighbor)) == 0) {
				allMoves[++allMoves[0]] = (short) ((square << 8) + (neighbor & 0xFF));
			}
		}
	}

